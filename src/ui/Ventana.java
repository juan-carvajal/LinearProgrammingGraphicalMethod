    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            this.initModel(Double.parseDouble(this.jFormattedTextField1.getText().replace(',', '.')), Double.parseDouble(this.jFormattedTextField2.getText().replace(',', '.')), this.jComboBox1.getSelectedItem().toString(), !this.jCheckBox1.isSelected());
        this.graphCanvas1.loadModel(model);
        this.graphCanvas1.repaint();
        Point p=model.giveOptimumPoint();
        if(p!=null){
            if(model.type.equals(Model.MAXIMIZE)){
                JOptionPane.showMessageDialog(this,"Maximum point ("+p.x+","+p.y+")");
            }else{
                JOptionPane.showMessageDialog(this,"Minimum point ("+p.x+","+p.y+")");
            }
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

