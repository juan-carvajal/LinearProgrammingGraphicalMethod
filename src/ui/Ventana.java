    public void initModel(double coefX, double coefY, String type, boolean allowNegatives){
        model=new Model(coefX, coefY, allowNegatives, type);
        for (int i = 0; i < this.jPanel5.getComponentCount(); i++) {
            ConstraintPanel p=(ConstraintPanel)this.jPanel5.getComponent(i);
            
            
            model.addConstraint(new Constraint(i+1, p.getXCoefficient(), p.getYCoefficient(), p.getRightCoefficient(), p.getConstraintType()));
        }
        System.out.println(model.toString());
       Point p= model.giveOptimumPoint();
       
           System.out.println(p);
       
    }

